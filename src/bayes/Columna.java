package bayes;

import java.util.ArrayList;

/**
 *
 * @author dell
 */
public class Columna {
    public String name;
    public ArrayList<String> datos = new ArrayList<>();

    public Columna(String name) {
        this.name = name;
    }
    @Override
    public String toString(){
        String s = name + " = ";
        for (String dato : datos) {
            s += dato + " ";
        }
        return s;
    }
}
